// Generate the classification scoreboard (docs/evals/classification-scoreboard.md).
// Replays the committed VLM extractions and runs the ADR-0009 classifier over
// them with a LIVE embedder, then routes each pair through the same
// reconciliation the scan pipeline uses. Runs in record-missing mode: already-
// recorded calls (the extract-v1 set, and typed re-extractions once recorded)
// replay from disk; anything new is recorded live and should be committed.
//
// Usage: pnpm build && node packages/evals/scripts/classify.mjs
// Requires Ollama with nomic-embed-text pulled (and qwen3-vl:2b for any
// not-yet-recorded typed re-extractions).

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { OllamaEmbeddingProvider, OllamaProvider } from '@outtray/core';
import { classifyEval } from '../dist/classify-eval.js';
import { RecordingProvider } from '../dist/recording-provider.js';

const here = dirname(fileURLToPath(import.meta.url));
const evalsRoot = join(here, '..');
const out = join(evalsRoot, '..', '..', 'docs', 'evals', 'classification-scoreboard.md');

const model = new RecordingProvider({
  dir: join(evalsRoot, 'recordings'),
  mode: 'record-missing',
  inner: new OllamaProvider(),
});
const embedder = new OllamaEmbeddingProvider({ model: 'nomic-embed-text' });

const report = await classifyEval(model, embedder, { model: 'qwen3-vl:2b' });

const pct = (a) => (a.n === 0 ? '-' : `${a.k}/${a.n} (${Math.round((a.k / a.n) * 100)}%)`);
const rows = report.perType
  .map((t) => `| ${t.type} | ${pct(t.vlm)} | ${pct(t.classifier)} | ${pct(t.reconciled)} |`)
  .join('\n');

const md = `# Classification scoreboard

Document-type classification accuracy across the pipeline (issue #37): the
one-shot VLM label, the ADR-0009 on-device k-NN classifier run over the VLM's
extraction text (k=3), and the reconciled effective type after the accepted
ADR-0009 amendment's routing, including one typed re-extraction on a confident
disagreement.

Model \`qwen3-vl:2b\` (replayed, extract-v1 and extract-typed-v1 recordings) +
embedder \`nomic-embed-text\` (live). Generated by
\`node packages/evals/scripts/classify.mjs\`. Not yet CI-replayed: embedding
record/replay is a follow-up (issue #52), so these are dev-run numbers.

| Document type | VLM type accuracy | Classifier accuracy | Reconciled accuracy |
| --- | --- | --- | --- |
${rows}
| **overall** | **${pct(report.vlm)}** | **${pct(report.classifier)}** | **${pct(report.reconciled)}** |

The classifier is seeded only (no user corrections yet); personalization from
the ADR-0008 correction loop layers on top.
`;
writeFileSync(out, md);
process.stdout.write(
  `classification: VLM ${pct(report.vlm)}, classifier ${pct(report.classifier)}, reconciled ${pct(report.reconciled)}\n`,
);
for (const r of report.rows) {
  process.stdout.write(
    `  ${r.id.padEnd(16)} label=${r.label} vlm=${r.vlm} clf=${r.classifier} reconciled=${r.reconciled} (${r.status})\n`,
  );
}
